def test_mnist_similarity():
    # Full training dataset
    d0 = TensorFlowDataset(train_data) # pyright: ignore[reportArgumentType]
    # Half training dataset
    d1 = TensorFlowDataset(itertools.islice(train_data, 0, len(train_data) // 2)) # pyright: ignore[reportArgumentType]
    # Merged training + test datasets
    d2 = TensorFlowDataset(itertools.chain(train_data, test_data)) # pyright: ignore[reportArgumentType]
    # Full test dataset
    d3 = TensorFlowDataset(test_data) # pyright: ignore[reportArgumentType]

    # Identical datasets
    assert_fingerprint_similarity(d0, d0, 1.0)
    # Dataset vs. half dataset
    assert_fingerprint_similarity(d0, d1, 0.5)
    # Completely different datasets
    assert_fingerprint_similarity(d0, d3, 0.0)
    # Train + test datasets vs. test dataset only
    # Ratio if 60000+10000 = 70000 vs. 10000 data points
    # Expected similarity is 1/7 ~= 0.1428
    assert_fingerprint_similarity(d2, d3, 0.1428)


def test_serialization():
    # Get the first element from the dataset
    sample = next(iter(test_data))
    img: np.ndarray = sample["image"].numpy()
    label: int = int(sample["label"].numpy())
    serialized = TensorFlowDataset.serialize_data_point({"image": img, "label": label})
    deserialized = TensorFlowDataset.deserialize_data_point(serialized)

    assert label == deserialized["label"]
    assert np.array_equal(img, deserialized["image"])
