def test_similarity(config):
    data1 = extract_csv_range(csv_file, 0, 5000, "/tmp/data1.csv")
    data2 = extract_csv_range(csv_file, 5000, 10000, "/tmp/data2.csv")
    data3 = extract_csv_range(csv_file, 0, 10000, "/tmp/data3.csv")
    data4 = extract_csv_range(csv_file, 5000, 15000, "/tmp/data4.csv")

    c1 = CanonicalDataset(CSVDataset(data1), config=config)
    c2 = CanonicalDataset(CSVDataset(data2), config=config)
    c3 = CanonicalDataset(CSVDataset(data3), config=config)
    c4 = CanonicalDataset(CSVDataset(data4), config=config)

    # Identical datasets
    assert_fingerprint_similarity(c1, c1, 1.0)
    # Dataset vs. half dataset
    assert_fingerprint_similarity(c1, c3, 0.5)
    # Completely different datasets
    assert_fingerprint_similarity(c1, c2, 0.0)
    # 1/3th in common
    assert_fingerprint_similarity(c3, c4, 0.33)


