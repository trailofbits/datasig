def test_mnist_similarity():
    # Full training dataset
    d0 = TorchVisionDataset(train_data) # pyright: ignore[reportArgumentType]
    # Half training dataset
    d1 = TorchVisionDataset(itertools.islice(train_data, 0, len(train_data) // 2)) # pyright: ignore[reportArgumentType]
    # Merged training + test datasets
    d2 = TorchVisionDataset(itertools.chain(train_data, test_data)) # pyright: ignore[reportArgumentType]
    # Full test dataset
    d3 = TorchVisionDataset(test_data) # pyright: ignore[reportArgumentType]

    # Identical datasets
    assert_fingerprint_similarity(d0, d0, 1.0)
    # Dataset vs. half dataset
    assert_fingerprint_similarity(d0, d1, 0.5)
    # Completely different datasets
    assert_fingerprint_similarity(d0, d3, 0.0)
    # Train + test datasets vs. test dataset only
    # Ratio if 60000+10000 = 70000 vs. 10000 data points
    # Expected similarity is 1/7 ~= 0.1428
    assert_fingerprint_similarity(d2, d3, 0.1428)


def test_serialization():
    img: Image.Image = test_data[0][0]
    label: int = test_data[0][1]
    serialized = TorchVisionDataset.serialize_data_point(img, label)
    deserialized_img, deserialized_label = TorchVisionDataset.deserialize_data_point(serialized)

    assert label == deserialized_label
    assert img.mode == deserialized_img.mode
    assert img.size == deserialized_img.size

    # Compare images using the PIL library directly
    diff = ImageChops.difference(img, deserialized_img)
    # If they are identical, getbbox should return None/False
    assert not diff.getbbox()
