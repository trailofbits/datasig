@pytest.mark.parametrize("config", [ConfigV0()])
def test_mnist_similarity(config):
    # Full training dataset
    d0 = TorchVisionDataset(train_data)
    c0 = CanonicalDataset(d0, config=config)
    # Half training dataset
    d1 = TorchVisionDataset(itertools.islice(train_data, 0, len(train_data) // 2))
    c1 = CanonicalDataset(d1, config=config)
    # Merged training + test datasets
    d2 = TorchVisionDataset(itertools.chain(train_data, test_data))
    c2 = CanonicalDataset(d2, config=config)
    # Full test dataset
    d3 = TorchVisionDataset(test_data)
    c3 = CanonicalDataset(d3, config=config)

    # Identical datasets
    assert_fingerprint_similarity(c0, c0, 1.0)
    # Dataset vs. half dataset
    assert_fingerprint_similarity(c0, c1, 0.5)
    # Completely different datasets
    assert_fingerprint_similarity(c0, c3, 0.0)
    # Train + test datasets vs. test dataset only
    # Ratio if 60000+10000 = 70000 vs. 10000 data points
    # Expected similarity is 1/7 ~= 0.1428
    assert_fingerprint_similarity(c2, c3, 0.1428)


def test_serialization():
    dataset = TorchVisionDataset(test_data)
    d = dataset[0]
    serialized = dataset.serialize_data_point(d)
    deserialized = TorchVisionDataset.deserialize_data_point(serialized)

    assert(d.mode == deserialized.mode)
    assert(d.size == deserialized.size)

    # Compare images using the PIL library directly
    diff = ImageChops.difference(d, deserialized)
    # If they are identical, getbbox should return None/False
    assert(not diff.getbbox())

